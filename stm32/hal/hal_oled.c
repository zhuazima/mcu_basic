void hal_Oled_ColorTurn(unsigned char i)
{
	if(i==0)
		{
			hal_Oled_WR_Byte(0xA6,OLED_CMD);//正常显示
		}
	if(i==1)
		{
			hal_Oled_WR_Byte(0xA7,OLED_CMD);//反色显示
		}
}

//屏幕旋转180度
void hal_Oled_DisplayTurn(unsigned char i)
{
	if(i==0)
		{
			hal_Oled_WR_Byte(0xC8,OLED_CMD);//正常显示
			hal_Oled_WR_Byte(0xA1,OLED_CMD);
		}
	if(i==1)
		{
			hal_Oled_WR_Byte(0xC0,OLED_CMD);//反转显示
			hal_Oled_WR_Byte(0xA0,OLED_CMD);
		}
}

static void hal_Oled_WR_Byte(unsigned char dat,unsigned char cmd)		//硬件实现SPI 模式 3
{		
	unsigned char retry=0;		
	if(cmd)
	{
		OLED_DC_Set();
	}else 
	{
		OLED_DC_Clr();		  
	}
	 
	SPI1->DR=dat;	 	  //发送一个byte 
			 
	retry=0;
 
	while((SPI1->SR & SPI_I2S_FLAG_BSY) != RESET)		//如果移植发不出去，稍微延时，防止死在这里
	{
		retry++;
		if(retry>200)
			return;
	}	  						    
 		 		  
	OLED_DC_Set();   	  
}



